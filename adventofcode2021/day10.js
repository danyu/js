let ERROR_SCORE_MAP = {
  ")": 3,
  "]": 57,
  "}": 1197,
  ">": 25137,
};
let COMPLETE_SCORE_MAP = {
  ")": 1,
  "]": 2,
  "}": 3,
  ">": 4,
};

let open = {
  "(": ")",
  "[": "]",
  "{": "}",
  "<": ">",
};

let close = {
  ")": "(",
  "]": "[",
  "}": "{",
  ">": "<",
};

function errorScore_part1(input) {
  let score = 0;
  const getScore = (str) => {
    let stk = [str[0]];
    const peek = () => stk[stk.length - 1];
    let i = 1;
    while (i < str.length) {
      if (close[str[i]]) {
        if (close[str[i]] !== peek()) {
          //   console.log(close[str[i]]);
          return ERROR_SCORE_MAP[str[i]];
        } else {
          stk.pop();
        }
      } else stk.push(str[i]);
      i++;
    }
    return 0;
  };
  input.forEach((line) => {
    score += getScore(line);
  });
  return score;
}

function completeLineScore_part2(input) {
  let scores = [];
  const calCompScore = (str) => {
    let stk = [str[0]];
    const peek = () => stk[stk.length - 1];
    let i = 1;
    while (i < str.length) {
      if (close[str[i]]) {
        if (close[str[i]] !== peek()) {
          //   console.log(close[str[i]]);
          return 0;
        } else {
          stk.pop();
        }
      } else stk.push(str[i]);
      i++;
    }
    // console.log(stk);
    let curScore = 0;
    let end = stk.length - 1;
    while (end > -1) {
      curScore = curScore * 5 + COMPLETE_SCORE_MAP[open[stk[end]]];
      //   console.log(curScore);
      end--;
    }
    // console.log(stk, curScore);
    return curScore;
  };

  input.forEach((line) => {
    let score = calCompScore(line);
    if (score) scores.push(score);
  });
  //   console.log(scores);
  scores.sort((a, b) => a - b);
  return scores[(scores.length - 1) / 2];
}

